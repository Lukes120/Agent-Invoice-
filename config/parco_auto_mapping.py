def get_info_by_targa(targa: str) -> Optional[dict]:
    """Ritorna info veicolo dato la targa (uppercase normalizzata)."""
    if not targa:
        return None
    return PARCO_BY_TARGA.get(targa.upper().strip())


def get_info_by_contratto(num: str) -> Optional[dict]:
    """Ritorna info veicolo dato il numero contratto."""
    if not num:
        return None
    return PARCO_BY_CONTRATTO.get(str(num).strip())


def get_classificazione_by_targa(targa: str) -> Optional[str]:
    """Ritorna classificazione fiscale (POOL/uso_promiscuo/super_lusso) o None."""
    info = get_info_by_targa(targa)
    return info["classificazione"] if info else None


def get_classificazione_by_contratto(num: str) -> Optional[str]:
    """Ritorna classificazione fiscale dato il numero contratto."""
    info = get_info_by_contratto(num)
    return info["classificazione"] if info else None
